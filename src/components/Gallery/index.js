class Gallery extends Component {
  state = {imageId: imagesList[0].id}

  getImage = () => {
    const {imageId} = this.state

    const filteredImage = imagesList.filter(eachItem => imageId === eachItem.id)

    return filteredImage
  }

  clickedThumbnail = id => {
    this.setState({imageId: id})
  }

  render() {
    const imageInfo = this.getImage()

    const {imageId} = this.state

    const {imageUrl, imageAltText} = imageInfo[0]
    return (
      <div className="body1">
        <div className="main">
          <img className="image" src={imageUrl} alt={imageAltText} />
          <div className="sub">
            <h1 className="h1">Nature Photography</h1>
            <p className="p1">Nature Photography by Rahul</p>
            <ul className="ul">
              {imagesList.map(eachItem => (
                <Thumbnail
                  thumbnailInfo={eachItem}
                  clicked={this.clickedThumbnail}
                  isActive={eachItem.id === imageId}
                  key={eachItem.id}
                />
              ))}
            </ul>
          </div>
        </div>
      </div>
    )
  }
}

export default Gallery
